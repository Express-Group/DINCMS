<p>இலங்கைக்கு எதிரான டெஸ்ட் தொடரை இந்திய அணி 1-0 என்கிற கணக்கில் வென்றது. இலங்கை பேட்ஸ்மேன்களின் அசத்தலான பேட்டிங் திறமையால் மூன்றாவது டெஸ்ட் போட்டி டிராவில் முடிவடைந்துள்ளது.</p>

<p>தில்லியில் இரு அணிகளுக்கு இடையே சனிக்கிழமை தொடங்கிய ஆட்டத்தில் டாஸ் வென்ற இந்தியா முதலில் பேட் செய்தது. 127.5 ஓவர்களில் 7 விக்கெட் இழப்புக்கு 536 ரன்கள் எடுத்திருந்தபோது டிக்ளேர் செய்தது இந்தியா. இதையடுத்து தனது முதல் இன்னிங்ஸைத் தொடங்கிய இலங்கை, 135.3 ஓவர்களில் 373 ரன்கள் எடுத்தது. இதையடுத்து, 163 ரன்கள் முன்னிலையுடன் இரண்டாவது இன்னிங்ஸைத் தொடங்கிய இந்தியா, 5 விக்கெட் இழப்புக்கு 246-ஆக ஸ்கோர் இருந்தபோது, டிக்ளேர் செய்வதாக அறிவித்தார் கோலி. 410 ரன்கள் எடுத்தால் வெற்றி என்ற இலக்குடன் இலங்கை இரண்டாவது இன்னிங்ஸை தொடங்கியது. தொடக்க வீரர்களாக களம் இறங்கிய கருணாரத்னே 13 ரன், சமரவிக்ரமா 5 ரன்களில் ஆட்டமிழந்தனர். சில்வாவுடன் ஜோடி சேர்ந்த லக்மல் ரன் எதுவுமின்றி பெவிலியன் திரும்பினார். 4-ஆவது நாள் ஆட்ட நேர முடிவில் இலங்கை அணி 16 ஓவர்களில் 3 விக்கெட் இழப்புக்கு 31 ரன்கள் எடுத்தது. சில்வா 13 ரன்களுடனும் மேத்யூஸ் ரன்கள் இன்றியும் களத்தில் இருந்தார்கள்.&nbsp;</p>

<p>இன்னும் 379 ரன்கள் எடுத்தால் வெற்றி என்ற இலக்குடன் 5-ஆவது நாளான இன்று இலங்கை அணி தனது 2-வது இன்னிங்ஸைத் தொடர்ந்தது. மேத்யூஸ் 1 ரன்னில் இருந்தபோது ஜடேஜாவின் பந்துவீச்சில் வீழ்ந்தார். ஜடேஜா அப்போது நோ பால் வீசினாலும் நடுவர்கள் அதைக் கவனிக்காததால் மேத்யூஸ் பெவிலியன் திரும்பவேண்டிய நிலைமை ஏற்பட்டது. அதன்பிறகு ஜோடி சேர்ந்த சில்வா - சண்டிமல் இந்திய அணியின் பந்துவீச்சைத் திறமையாக எதிர்கொண்டார்கள். இதனால் கடைசி நாளின் முதல் பகுதியில் மேலும் விக்கெட்டுகள் எதுவும் எடுக்கமுடியாத நிலைமை ஏற்பட்டது. உணவு இடைவேளையின்போது இலங்கை அணி 47 ஓவர்களில் 4 விக்கெட் இழப்புக்கு 119 ரன்கள் எடுத்தது.&nbsp;</p>

<p>உணவு இடைவேளைக்குப் பிறகு, 90 பந்துகள் வரை தாக்குப்பிடித்த சண்டிமல், அஸ்வினின் அற்புதமான பந்தில் போல்ட் ஆகி, 36 ரன்களில் வெளியேறினார். இதனால் இந்திய அணிக்கு அழுத்தம் கொடுத்த சண்டிமல் - டி சில்வா ஜோடி இதனால் பிரிந்தது.&nbsp;</p>

<p>தொடர்ந்து சிறப்பாக விளையாடி வந்த தனஞ்செய டி சில்வா, 188 பந்துகளில் சதமெடுத்தார். இது அவருடைய 3-வது சதமாகும். இந்த டெஸ்ட் போட்டியில் களமிறங்கியுள்ள ரோஷன் சில்வாவும் சண்டிமலின் விக்கெட்டுக்குப் பிறகு டி சில்வாவுக்கு நல்ல இணையாக விளங்கினார். இருவரும் நன்றாக விளையாடி வந்தபோது தனஞ்செய டி சில்வாவுக்குத் திடீரென காயம் ஏற்பட்டது. இதனால் 76-வது ஓவரின் முடிவில் 119 ரன்களில் ரிடையர்ட் ஹர்ட் முறையில் அவர் வெளியேறினார். இதையடுத்து நிரோஷன் டிக்வெல்லா களமிறங்கினார்.</p>

<p>தேநீர் இடைவேளையின்போது இலங்கை அணி 81 ஓவர்களில் 5 விக்கெட் இழப்புக்கு 226 ரன்கள் எடுத்தது. ரோஷன் சில்வா 38, நிரோஷன் டிக்வெல்லா 11 ரன்களுடன் களத்தில் இருந்தார்கள்.&nbsp;</p>

<p>பொதுவாக ஒரு டெஸ்ட் போட்டியின் கடைசி நாளின் கடைசிப் பகுதியில் விளையாடும் பேட்ஸ்மேன்கள் மிகுந்த அழுத்தத்தை உணர்வார்கள். கொல்கத்தா டெஸ்ட் போட்டியிலும் இதைக் காணமுடிந்தது. ஆனால் ரோஷன் சில்வாவும் டிக்வெல்லாவும் இந்திய பந்துவீச்சை அற்புதமாக கையாண்டார்கள். அஸ்வினும் ஜடேஜாவுக்கும் அதிக ஓவர்கள் கொடுத்து நெருக்கடி உண்டு பண்ண கோலி முயற்சி செய்தார். ஆனால் அவருடைய அனைத்துத் திட்டங்களையும் முறியடித்தார்கள். பல்வேறு முயற்சிகளைக் கையாண்ட பிறகும் ஒரு விக்கெட்டும் விழாததால் 103-வது ஓவரின் முடிவில் ஆட்டத்தை முடித்துகொள்ள இரு அணிகளும் ஒப்புக்கொண்டன. இலங்கை அணி தனது 2-வது இன்னிங்ஸில் 103 ஓவர்களில் 5 விக்கெட் இழப்புக்கு 299 ரன்கள் எடுத்தது. இந்திய பந்துவீச்சைத் திறமையாக எதிர்கொண்டு ரோஷன் சில்வா 74, நிரோஷன் டிக்வெல்லா 44 ரன்கள் எடுத்து கடைசிவரை ஆட்டமிழக்காமல் இருந்தார்கள். இதனால் 3-வது டெஸ்ட் போட்டி எதிர்பாராதவிதமாக டிராவில் முடிவடைந்தது.&nbsp;</p>

