}else{
document.getElementById('tabs-'+menuId).innerHTML = '<figure style="text-align: center;"><img src="https://images.dinamani.com/images/FrontEnd/images/menu-loader.gif" style="width: 70px;position: absolute;top: 43%;left: 57%;"></figure>';
